import cv2
image1 = cv2.imread('5.jpg',1)
image2 = cv2.imread('6.jpg',1)
blur = cv2.blur(image1,(3,3))
blurG = cv2.GaussianBlur(image1,(3,3),0)
median = cv2.medianBlur(image1,3)
blur2 = cv2.blur(image2,(3,3))
blurG2 = cv2.GaussianBlur(image2,(3,3),0)
median2 = cv2.medianBlur(image2,3)
cv2.imshow('main1',image1)
cv2.imshow('aVg1',blur)
cv2.imshow('gauss1',blurG)
cv2.imshow('median1',median)

cv2.imshow('main2',image2)
cv2.imshow('aVg2',blur2)
cv2.imshow('gauss2',blurG2)
cv2.imshow('median2',median2)
cv2.waitKey(0)
cv2.distroyAllWindows()